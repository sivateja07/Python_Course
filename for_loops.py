#a for loop iterates over a string or a list and in each iteration a loop variable stores or holds a single item (memory)
for item in ['python', 'siva', 'life', 'happy']:
    print(item)

for sake in "Sivateja":
    print(sake)

for bol in range(8): #useful fucntion when we need to print numbers from particular large number ranges, it excludes the last number
    print(bol)

for bol in range(5, 15): #sstart from range to end of range
    print(bol)

for sol in range(5,12, 3): #like a step counter ignores 3 items
    print(sol)